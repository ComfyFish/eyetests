def convert_to_base10(num):
    digits = list(map(int, str(num)))
    return (digits[0]*25) + (digits[1]*5) + (digits[2])

def convert_to_base5(num):
    s = ""
    while num:
        s = str(num % 5) + s
        num /= 5
    while len(s) < 3:
        s = "0" + s
    return s

def sort(array):
    maxrows = (int(math.ceil(len(array) / 78)))
    lines = [[0] * 78 for _ in range(maxrows)]
    i = 1
    j = 0
    while i < len(array)+1:
        lines[j][i - (j*78) - 1] = array[i-1]
        j = int(i / 78)
        i += 1
    del lines[j][i - (j*78) - 1:]
    i = 0
    while i < len(lines):
        half = int(math.ceil(len(lines[i])/2))
        arr1 = lines[i][:half]
        arr2 = lines[i][half:]
        arr2.reverse()
        lines[i] = arr1 + arr2
        i += 1
    return lines

def combine_sorted_array(array):
    new_arr = []
    i = 0
    while i < len(array):
        new_arr += array[i]
        i += 1
    return new_arr

def convert_to_tri(array):
    new_arr = [""]*(len(array)//3)
    i = 0
    j = 0
    while i < len(array):
        new_arr[j] += str(array[i])
        i += 1
        j = math.floor(i / 3)
    return new_arr

def tri_to_base10(array):
    new_arr = [0]*len(array)
    i = 0
    while i < len(array):
        new_arr[i] = convert_to_base10(array[i])
        i += 1
    return new_arr

